        // --- table ---
        glm::mat4 modeltable = glm::mat4(1.0f);
        modeltable = glm::translate(modeltable, glm::vec3(1.0f, 0.0f, 1.0f)); // posición
        modeltable = glm::scale(modeltable, glm::vec3(0.01f, 0.01f, 0.01f));    // tamaño
